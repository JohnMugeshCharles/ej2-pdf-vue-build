                  <td align="left">
                    <br />
                    <button id="viewtemplatebtn" class="e-btn" type="button" style="width: 150px; text-transform:capitalize">
                      View Template
                    </button>
                    <button id="topRedactBtn" class="e-btn" type="button"
                      style="width: 150px; text-transform:capitalize; margin-left: 8px">
