          <div class="actions">
            <button id="uploadRedactBtn" class="e-btn" type="button" style="width: 150px; text-transform:capitalize">
              Redact PDF
            </button>
          </div>
        </div>
      </div>
    </div>

    <div id="action-description">
      <p>This sample demonstrates redact a text from the PDF document. Redaction is a process of permanently removing sensitive or unwanted information from the PDF document.</p>
