<template>
  <div class="control-section">
    <div class="form-container">
      <label class="e-label" for="ps">Page Size :</label>
      <ejs-dropdownlist 
        id="ps" 
        v-model="formData.pageSize" 
        :dataSource="pageSizeOptions"
        :fields="{ text: 'text', value: 'value' }"
        floatLabelType="Never"
      ></ejs-dropdownlist>

      <label class="e-label" for="po">Page Orientation :</label>
      <ejs-dropdownlist 
        id="po" 
        v-model="formData.pageOrientation" 
        :dataSource="orientationOptions"
        :fields="{ text: 'text', value: 'value' }"
        floatLabelType="Never"
      ></ejs-dropdownlist>

      <label class="e-label" for="pm">Page Margin :</label>
      <ejs-dropdownlist 
        id="pm" 
        v-model="formData.pageMargin" 
        :dataSource="marginOptions"
        :fields="{ text: 'text', value: 'value' }"
        floatLabelType="Never"
      ></ejs-dropdownlist>

      <label class="e-label" for="pr">Page Rotation :</label>
      <ejs-dropdownlist 
        id="pr" 
        v-model="formData.pageRotation" 
        :dataSource="rotationOptions"
        :fields="{ text: 'text', value: 'value' }"
        floatLabelType="Never"
      ></ejs-dropdownlist>

      <label class="e-label">Pages Count :</label>
      <div class="pages-inline">
        <ejs-textbox 
          id="pageCount" 
          v-model="formData.pageCount" 
          :readonly="true"
          cssClass="page-count-input"
        ></ejs-textbox>
        <ejs-button id="addPage" @click="addPage" :isPrimary="false">Add Page</ejs-button>
      </div>
    </div>
    <br />
    <br />
    <div>
      <p>
        Click the button to view a PDF document generated by JavaScript PDF Library. Please note that Adobe Reader or its
        equivalent is required to view the resultant document.
      </p>
    </div>
    <div class="actions">
      <ejs-button id="normalbtn" @click="createPdf" :isPrimary="true">Generate PDF</ejs-button>
    </div>
    <div id="action-description">
      <p>
        This sample demonstrates adding pages with different settings such as rotation, orientation and page size.
      </p>
    </div>
    <div id="description">
      <p>It is also possible to add or insert pages and sections, remove pages, rearrange pages, and more. More information about the pages and sections can be found in this documentation <a href="https://help.syncfusion.com/document-processing/pdf/pdf-library/javascript/pdf-pages">section</a>.</p>  
    </div>
  </div>
</template>
